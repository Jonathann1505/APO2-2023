package Punto7;

public class Person {

	
	private int age;
	private String name;
	
	public Person(int age, String name) {
	
		super();
		this.age = age;
		this.name = name;
		
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}
	
}
